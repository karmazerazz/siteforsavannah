export const getRandomMessage = (category) => {
  const cat = INITIAL_MESSAGES[category] ? category : 'attention';
  
  // Get pool from localStorage
  const stored = localStorage.getItem(STORAGE_KEY);
  let pool = stored ? JSON.parse(stored) : null;

  // Initialize pool if empty or first time
  if (!pool) {
    pool = { ...INITIAL_MESSAGES };
    localStorage.setItem(STORAGE_KEY, JSON.stringify(pool));
  }

  // Ensure the category exists in the pool (in case of schema changes)
  if (!pool[cat]) {
    pool[cat] = [...(INITIAL_MESSAGES[cat] || INITIAL_MESSAGES['attention'])];
  }

  const categoryMessages = pool[cat];
  
  if (categoryMessages.length === 0) {
    // Should theoretically not happen if notification works, but let's be safe
    return "Mon amour, je n'ai plus de mots nouveaux pour l'instant, mais sache que je t'aime à l'infini. ❤️";
  }

  // Pick a random index
  const randomIndex = Math.floor(Math.random() * categoryMessages.length);
  const message = categoryMessages[randomIndex];

  // Remove the message from the pool
  categoryMessages.splice(randomIndex, 1);
  
  // Save updated pool
  localStorage.setItem(STORAGE_KEY, JSON.stringify(pool));

  // Check if depleted
  if (categoryMessages.length === 0) {
    const userPing = "<@1326084715807244368>";
    const alertMessage = `Il n'y as plus de message dans cette catégorie: ${cat} ${userPing}`;
    sendNotification(alertMessage);
  }

  return message;
};
